from typing import List, Tuple, Dict, Optional

from pace.utils.logging import PACE_LLM_WARNING


def _fmt_lp(logprobs: Dict[int, float], token: int) -> str:
    """Format a token's logprob value, or 'N/A' if absent."""
    val = logprobs.get(token)
    return f"{val:.4f}" if val is not None else "N/A"


def _rank_in(logprobs: Dict[int, float], token: int) -> str:
    """Return 1-based rank of token in logprobs dict, or 'N/A' if absent."""
    if token not in logprobs:
        return "N/A"
    sorted_toks = sorted(logprobs, key=logprobs.get, reverse=True)
    return str(sorted_toks.index(token) + 1)


TokensText = Tuple[List[int], str]
TokensTextLogprobs = Tuple[List[int], str, Optional[List[Dict[int, float]]]]


def check_outputs_equal(
    *,
    ref_outputs: List[TokensText],
    test_outputs: List[TokensText],
    ref_name: str,
    test_name: str,
) -> bool:
    """Compare two sequences of generated outputs for exact equality.

    Returns True if all outputs match, False otherwise.
    """
    if len(ref_outputs) != len(test_outputs):
        PACE_LLM_WARNING(
            f"Output count mismatch: {ref_name} produced {len(ref_outputs)} "
            f"outputs, {test_name} produced {len(test_outputs)}"
        )
        return False

    all_passed = True
    for prompt_idx, (ref, test) in enumerate(zip(ref_outputs, test_outputs)):
        ref_ids, ref_str = ref[0], ref[1]
        test_ids, test_str = test[0], test[1]

        if ref_str != test_str or ref_ids != test_ids:
            PACE_LLM_WARNING(
                f"Prompt {prompt_idx}:"
                f"\n  {ref_name}: {ref_str!r}"
                f"\n  {test_name}: {test_str!r}"
            )
            all_passed = False

    return all_passed


def check_logprobs_close(
    *,
    ref_outputs: List[TokensTextLogprobs],
    test_outputs: List[TokensTextLogprobs],
    ref_name: str,
    test_name: str,
    warn_on_mismatch: bool = True,
    always_check_logprobs: bool = False,
) -> bool:
    """Compare logprobs of two sequences generated by different backends.

    The check is symmetric: each backend's chosen token must appear in the
    other's top-k logprobs for a mismatch to be tolerated.

    For each generated token position:
      - If tokens match and always_check_logprobs is False, skip logprobs check.
      - If tokens differ, verify each backend's chosen token appears in the
        other's top-k logprobs. On the first divergence that fails this
        cross-membership check, the comparison stops for that prompt.

    Returns True if all prompts pass, False otherwise.
    """
    if len(ref_outputs) != len(test_outputs):
        PACE_LLM_WARNING(
            f"Output count mismatch: {ref_name} produced {len(ref_outputs)} "
            f"outputs, {test_name} produced {len(test_outputs)}"
        )
        return False

    all_passed = True

    for prompt_idx, (ref, test) in enumerate(zip(ref_outputs, test_outputs)):
        ref_ids, ref_str, ref_logprobs = ref
        test_ids, test_str, test_logprobs = test

        if ref_logprobs is None:
            ref_logprobs = [None] * len(ref_ids)
        if test_logprobs is None:
            test_logprobs = [None] * len(test_ids)

        # bf16 rounding across many layers can shift token probabilities enough
        # to trigger an early EOS; compare the overlapping prefix instead of
        # failing outright on length mismatches.
        length_mismatch = len(ref_ids) != len(test_ids)
        if length_mismatch:
            PACE_LLM_WARNING(
                f"Prompt {prompt_idx}: length mismatch -- "
                f"{ref_name} generated {len(ref_ids)} tokens, "
                f"{test_name} generated {len(test_ids)} tokens"
            )

        min_len = min(len(ref_ids), len(test_ids))

        for idx, (ref_tok, test_tok) in enumerate(
            zip(ref_ids[:min_len], test_ids[:min_len])
        ):
            is_tok_mismatch = ref_tok != test_tok

            if not (is_tok_mismatch or always_check_logprobs):
                continue

            ref_lp = ref_logprobs[idx] if idx < len(ref_logprobs) else None
            test_lp = test_logprobs[idx] if idx < len(test_logprobs) else None

            if ref_lp is None or test_lp is None:
                if warn_on_mismatch and is_tok_mismatch:
                    PACE_LLM_WARNING(
                        f"Prompt {prompt_idx}, position {idx}: "
                        f"token mismatch without logprobs -- "
                        f"{ref_name} chose {ref_tok}, "
                        f"{test_name} chose {test_tok}"
                    )
                all_passed = False
                break

            ref_in_test = ref_tok in test_lp
            test_in_ref = test_tok in ref_lp

            if not (ref_in_test and test_in_ref):
                missing = []
                if not ref_in_test:
                    missing.append(
                        f"{ref_name}'s token {ref_tok} not in " f"{test_name}'s top-k"
                    )
                if not test_in_ref:
                    missing.append(
                        f"{test_name}'s token {test_tok} not in " f"{ref_name}'s top-k"
                    )
                PACE_LLM_WARNING(
                    f"Prompt {prompt_idx}, position {idx}: "
                    f"logprob cross-check FAILED -- {'; '.join(missing)}"
                    f"\n  {ref_name}: token={ref_tok} "
                    f"(logprob={_fmt_lp(ref_lp, ref_tok)})"
                    f"\n  {test_name}: token={test_tok} "
                    f"(logprob={_fmt_lp(test_lp, test_tok)})"
                )
                all_passed = False
                break

            if warn_on_mismatch:
                ref_val = ref_lp.get(ref_tok, 0.0)
                test_val = test_lp.get(test_tok, 0.0)
                PACE_LLM_WARNING(
                    f"Prompt {prompt_idx}, position {idx}: "
                    f"token mismatch (tolerated, both in each other's top-k)"
                    f"\n  {ref_name}: token={ref_tok} "
                    f"(logprob={_fmt_lp(ref_lp, ref_tok)}, "
                    f"rank {_rank_in(test_lp, ref_tok)} in {test_name}'s top-k)"
                    f"\n  {test_name}: token={test_tok} "
                    f"(logprob={_fmt_lp(test_lp, test_tok)}, "
                    f"rank {_rank_in(ref_lp, test_tok)} in {ref_name}'s top-k)"
                    f"\n  delta={abs(ref_val - test_val):.4f}"
                )
            break
        else:
            if ref_str != test_str and warn_on_mismatch:
                PACE_LLM_WARNING(
                    f"Prompt {prompt_idx}: tokens match but text differs:"
                    f"\n  {ref_name}: {ref_str!r}"
                    f"\n  {test_name}: {test_str!r}"
                )

    return all_passed
